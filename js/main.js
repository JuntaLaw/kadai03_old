const resetButton = document.querySelector(`#reset-button`);
const tarotImage = document.querySelector(`#tarot-image`);
const tarotTitle = document.querySelector(`.tarot-title`); 
const tarotText = document.querySelector(`.tarot-text`); 
const tarotKeyword = document.querySelector(`.tarot-keyword`); 
const tarotScore = document.querySelector(`.tarot-score`); 
const results = document.querySelector(`.results`); 
const buttonArea = document.querySelector(`.buttonArea`); 

let resultIndex = -1;

// タロットを引く関数
function drawFortune() {
    resultIndex = Math.floor(Math.random() * arcanaData.length);
    const result = arcanaData[resultIndex];
    tarotImage.src = result.image;
    results.style.display = "block";
    tarotTitle.textContent = result.title; 
    tarotText.textContent = result.text; 
    tarotKeyword.textContent = `キーワード ・・・${result.keyword}`; 
    tarotScore.textContent = `幸運度・・・${result.score}点 / （10点満点）`; 
    tarotImage.style.display = "block";
    tarotButton.style.display = "none";  
    resetButton.style.display = "block"; 
}

// リセットボタンのクリックイベントを追加
resetButton.addEventListener("click", function() { 
    results.style.display = "none"; 
    tarotImage.style.display = "none";
    tarotButton.style.display = "block";  
    resetButton.style.display = "block";
    resultIndex = -1;
});

// タロットを引くボタンのクリックイベントを追加
tarotButton.addEventListener("click", drawFortune);

